export function buildTitlePrompt(theme) {
  return `あなたはnoteで多くの読者を集めるプロのライターです。
以下のテーマについて、バズるタイトルを5案生成してください。

## テーマ
${theme}

## タイトル生成ルール（バズの法則）
1. 20〜32文字の長さにする
2. 数字を入れる（「3つの」「30分で」「99%の人が」等）
3. 好奇心フックを使う（「本当は教えたくない」「誰でもできる」「〇〇の裏側」「知らないと損する」等）
4. 読者のメリットを明示する（読んだら何が得られるか）
5. 感情に訴える（驚き・共感・不安→解決）
6. 具体的にする（抽象的な表現を避ける）

## 出力形式
以下のJSON形式で出力してください。JSON以外のテキストは含めないでください。

[
  "タイトル1",
  "タイトル2",
  "タイトル3",
  "タイトル4",
  "タイトル5"
]`;
}
