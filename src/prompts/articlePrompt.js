export function buildArticlePrompt(theme, title) {
  return `あなたはnoteで10万PVを超える記事を量産しているプロライターです。
以下のテーマとタイトルに基づいて、note向けの記事本文を生成してください。

## テーマ
${theme}

## タイトル
${title}

## 重要：記事の長さ
- 必ず3000文字以上、5000文字以下で書いてください
- 見出しは最低5つ以上使ってください
- 各見出しの下には最低200文字以上の本文を書いてください
- 短い記事は絶対にNGです

## 記事構成テンプレート（この順番で書くこと）

### 1. 導入（400文字以上）
- 読者の悩みや課題に共感するエピソードから始める
- 「あなたもこんな経験ありませんか？」のような問いかけ
- この記事を読むと何が得られるかを明示する

### 2. 本論（2000文字以上、見出し3〜5個）
- 各セクションに具体的な手順、事例、数字を入れる
- 「例えば〜」「実際に〜」「具体的には〜」を多用する
- 抽象論ではなく、読者がすぐ実践できる内容にする
- 自分の体験談や感想を交えてリアリティを出す

### 3. まとめ（300文字以上）
- 記事の要点を箇条書きで振り返る
- 読者への具体的なアクションを提案する
- 「スキ」やフォローを促す一言を自然に入れる

## 文体ルール
- 「です・ます」調で統一
- 一文は60文字以内を目安にする
- 難しい専門用語は噛み砕いて説明する
- 箇条書きと文章をバランスよく使う
- 感情表現を入れる（「正直、驚きました」「これが本当に便利で」等）

## 画像プレースホルダー
本文中の適切な箇所に [IMAGE: 画像の説明] を2〜3個入れてください。

## 出力形式
マークダウン形式で記事本文のみを出力してください。タイトルは含めないでください。
短い記事を出力しないでください。必ず3000文字以上書いてください。`;
}
