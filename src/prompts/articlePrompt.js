export function buildArticlePrompt(theme, title) {
  return `あなたはnoteで人気のあるプロのライターです。
以下のテーマとタイトルに基づいて、note向けの記事本文を生成してください。

## テーマ
${theme}

## タイトル
${title}

## 記事生成ルール
1. note読者に刺さる、親しみやすく読みやすい文体で書く
2. 冒頭で読者の共感を引き出す導入を書く
3. 見出し（##）を使って構成を明確にする
4. 具体的なエピソードや例を入れる
5. 箇条書きを適度に使い、読みやすくする
6. 最後に読者への問いかけやアクションを促すまとめを書く
7. 2000〜4000文字程度の長さにする
8. 画像を挿入すべき箇所に [IMAGE: 画像の説明] というプレースホルダーを2〜3個入れる

## 出力形式
マークダウン形式で記事本文のみを出力してください。タイトルは含めないでください。`;
}
